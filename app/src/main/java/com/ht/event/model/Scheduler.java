package com.ht.event.model;


public class Scheduler {

    public String month;
    public String day;
    public String date;
    public String time;
    public String detailing;

    public String getMonth(String s) {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getDay(String s) {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getDate(String s) {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime(String s) {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDetailing(String s) {
        return detailing;
    }

    public void setDetailing(String detailing) {
        this.detailing = detailing;
    }
}
